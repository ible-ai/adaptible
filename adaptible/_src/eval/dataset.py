"""Dataset structures for evaluation."""

import dataclasses
import json
from pathlib import Path
from typing import Sequence


@dataclasses.dataclass
class TriviaItem:
    """A single trivia question with ground truth."""

    id: str
    category: str
    question: str
    correct_answer: str
    key_terms: list[str] = dataclasses.field(default_factory=list)
    difficulty: str = "medium"  # easy, medium, hard
    notes: str = ""

    def to_dict(self) -> dict:
        return dataclasses.asdict(self)

    @classmethod
    def from_dict(cls, data: dict) -> "TriviaItem":
        return cls(**data)


@dataclasses.dataclass
class TriviaDataset:
    """Collection of trivia items."""

    name: str
    version: str
    items: list[TriviaItem] = dataclasses.field(default_factory=list)

    def __len__(self) -> int:
        return len(self.items)

    def __getitem__(self, idx: int) -> TriviaItem:
        return self.items[idx]

    def __iter__(self):
        return iter(self.items)

    def subset(self, indices: Sequence[int]) -> "TriviaDataset":
        """Create a subset of the dataset."""
        return TriviaDataset(
            name=f"{self.name}_subset",
            version=self.version,
            items=[self.items[i] for i in indices],
        )

    def by_category(self, category: str) -> "TriviaDataset":
        """Filter by category."""
        return TriviaDataset(
            name=f"{self.name}_{category}",
            version=self.version,
            items=[item for item in self.items if item.category == category],
        )

    def by_difficulty(self, difficulty: str) -> "TriviaDataset":
        """Filter by difficulty."""
        return TriviaDataset(
            name=f"{self.name}_{difficulty}",
            version=self.version,
            items=[item for item in self.items if item.difficulty == difficulty],
        )

    @property
    def categories(self) -> set[str]:
        return {item.category for item in self.items}

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "version": self.version,
            "items": [item.to_dict() for item in self.items],
        }

    @classmethod
    def from_dict(cls, data: dict) -> "TriviaDataset":
        return cls(
            name=data["name"],
            version=data["version"],
            items=[TriviaItem.from_dict(item) for item in data["items"]],
        )


def save_dataset(dataset: TriviaDataset, path: str | Path) -> None:
    """Save dataset to JSON file."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w") as f:
        json.dump(dataset.to_dict(), f, indent=2)


def load_dataset(path: str | Path) -> TriviaDataset:
    """Load dataset from JSON file."""
    with open(path) as f:
        return TriviaDataset.from_dict(json.load(f))


